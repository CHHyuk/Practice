"""
정수 N이 입력되면 00시00분00초부터 N시59분59초까지 모든 시각 중에
3이 하나라도 포함되는 모든 정수의 수를 구하는 프로그램을 작성하세요
"""

# 이 문제는 가능한 모든 시각의 경우를 하나씩 모두 세서 풀 수 있는 문제(완전탐색 문제)
# 파이썬은 1초에 2천만번 수행한다 생각하면 됨

n = int(input())

count = 0
for i in range(n+1):
    for j in range(60):
        for k in range(60):
            # 매 시각 안에 '3'이 포함되어 있다면 카운트 증가
            if '3' in str(i) + str(j) + str(k):
                count += 1
print(count)