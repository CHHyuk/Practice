# 숫자 0을 제외한 모든 수 : True
# 빈 딕셔너리, 빈 리스트를 제외한 모든 딕셔너리, 리스트 : True
# 아무 값도 없는 의미인 None : False
# 빈 문자열을 제외한 모든 문자열 : True

value = input('입력해 주세요> ') or '아무것도 못받았어' # 단락평가로 인해 앞에 인풋값이 있으면 True가 되어 바로 밑에 print 출력
                                                    # 아무 값도 입력하지 않으면 인풋값이 False가 됨
print('입력받은 값> ',value)